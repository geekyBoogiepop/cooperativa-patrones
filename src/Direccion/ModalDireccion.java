    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        // TODO add your handling code here:
        if (this.tipodir.getText().equals("")|| this.pais.getText().isEmpty()
                || this.departamento.getText().isEmpty() || this.ciudad.getText().isEmpty()
                || this.bcomarca.getText().isEmpty() || this.ddomiciliar.getText().isEmpty()
                || this.tdireccion.getText().isEmpty()) {
            ErrorAlert er = new ErrorAlert(new JFrame(), true);
            er.titulo.setText("OOPS...");
            er.msj.setText("FALTAN CAMPOS DE LLENAR");
            er.msj1.setText("");
            er.setVisible(true);
        } else {
            if (this.registrar.getText().equals("GUARDAR")) {
                
            } else {
                if (Opciones.verificaNumero(this.tipodir.getText(), this.CodS, this.pais.getText(),
                        this.departamento.getText(), this.ciudad.getText(), this.bcomarca.getText(),
                        this.ddomiciliar.getText(), this.tdireccion.getText())) {
                    ErrorAlert er = new ErrorAlert(new JFrame(), true);
                    er.titulo.setText("OOPS...");
                    er.msj.setText("LA DIRECCIÓN ");
                    er.msj1.setText("YA ÉXISTE");
                    er.setVisible(true);
                } else {
                    Sentencias s = new Sentencias();
                    s.setCodigoSocio(CodS);
                    s.setTipoDireccion(tdireccion.getText());
                    s.setPais(pais.getText());
                    s.setDepartamento(departamento.getText());
                    s.setCiudad(ciudad.getText());
                    s.setBComarca(bcomarca.getText());
                    s.setDirDomiciliar(ddomiciliar.getText());
                    s.setTipoPropiedad(tdireccion.getText());
                    int opcion = Opciones.registrar(s);
                    if (opcion != 0) {
                        limpiarCampos();
                        SuccessAlert sa = new SuccessAlert(new JFrame(), true);
                        sa.titulo.setText("¡HECHO!");
                        sa.msj.setText("SE HA REGISTRADO UNA");
                        sa.msj1.setText("NUEVA DIRECCIÓN");
                        sa.setVisible(true);
                    }
                }
            }
        }
    }//GEN-LAST:event_registrarActionPerformed

